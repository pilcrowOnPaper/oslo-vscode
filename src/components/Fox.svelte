<script>
    import {colorMap, boopCount} from "../javascript/stores"

    let textActivity;
    $: textActivity = $colorMap.get("activity bar text")
    import {db} from "../javascript/firebase"
    import firebase from 'firebase/app';

    db.collection("pilcrow").doc("boopCount")
    .get().then((doc) => {
